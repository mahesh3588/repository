    private void setCustomerValues(Customer customer) {
        try {
            textAddress.setText(customer.getAddress());
            textContact1.setText(customer.getContact1());
            textContact2.setText(customer.getContact2());
            textEmailId.setText(customer.getEmailId());
            textName.setText(customer.getName());
            textDateOfBirth.setText(customer.getBirthDate().toString());
        } catch (Exception e) {
        }
       
    }
    
