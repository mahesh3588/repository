    private void buttonModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonModifyActionPerformed
        Customer customer=new Customer();
        customer.setId(customerId);
        customer.setName(textName.getText());
        customer.setAddress(textAddress.getText());
        customer.setContact1(textContact1.getText());
        customer.setContact2(textContact2.getText());
        customer.setEmailId(textEmailId.getText());

        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date convertedCurrentDate = sdf.parse("2013-09-18");
            customer.setBirthDate(new java.sql.Date(convertedCurrentDate.getTime()));
        } catch (ParseException ex) {
            ex.printStackTrace();
            //Logger.getLogger(AddCustomerPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

        Boolean isUpdated = new CustomerClient().update(customer);
        if(isUpdated){
            JOptionPane.showMessageDialog(null, "Customer details modified successfully" );
            //clearText();
        }else{
            JOptionPane.showMessageDialog(null, "Sorry ! Unable to modify  customer details");
        }

    }//GEN-LAST:event_buttonModifyActionPerformed

